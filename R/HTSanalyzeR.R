HTSanalyzeR<-function(
	x,
	scoreSign="-",
	scoreMethod="zscore",
	summarizeMethod="mean",
	annotationColumn="GeneID",
	species=c("Drosophila_melanogaster"),
	initialIDs="FlybaseCG",
	duplicateRemoverMethod="max",
	orderAbsValue=FALSE,
	listOfGeneSetCollections,
	cutoffHitsEnrichment=2,
	pAdjMethod="BH",
	nPermutations=1000,
	minGeneSetSize=15,
	exponent=1,
	whichSetIsKEGGIds,
	whichSetIsGOIds,
	nwStatsControls="neg",
	nwStatsAlternative="two.sided",
	nwStatsTests=c("T-test"),
	nwStatsColumns=c("t.test.pvalues.two.samples","t.test.pvalues.one.sample"),
	nwAnalysisFdr=0.001,
	nwAnalysisGenetic=FALSE,
	biogridObject=NA,
	nwAnalysisOrder=2,
	nGseaPlots=10
	)
	{
#check that 'normCellHTSobject' is a cellHTS cellHTSobject
	if(class(x) != "cellHTS") stop("The argument 'x' is not a cellHTS object")
	HTSanalysis4CellHTS2(
		normCellHTSobject=x,
		scoreSign=scoreSign,
		scoreMethod=scoreMethod,
		summarizeMethod=summarizeMethod,
		annotationColumn=annotationColumn,
		species=species,
		initialIDs=initialIDs,
		duplicateRemoverMethod=duplicateRemoverMethod,
		orderAbsValue=orderAbsValue,
		listOfGeneSetCollections=listOfGeneSetCollections,
		cutoffHitsEnrichment=cutoffHitsEnrichment,
		pAdjMethod=pAdjMethod,
		nPermutations=nPermutations,
		minGeneSetSize=minGeneSetSize,
		exponent=exponent,
		whichSetIsKEGGIds=whichSetIsKEGGIds,
		whichSetIsGOIds=whichSetIsGOIds,
		nwStatsControls=nwStatsControls,
		nwStatsAlternative=nwStatsAlternative,
		nwStatsTests=nwStatsTests,
		nwStatsColumns=nwStatsColumns,
		nwAnalysisFdr=nwAnalysisFdr,
		nwAnalysisGenetic=nwAnalysisGenetic,
		biogridObject=biogridObject,
		nwAnalysisOrder=nwAnalysisOrder,
		nGseaPlots=nGseaPlots
		)

	}	